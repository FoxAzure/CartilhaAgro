const listaOperacoes = document.getElementById("listaOperacoes");

function carregarOperacoes() {
    const salvas = JSON.parse(localStorage.getItem("minhasOperacoes") || "[]");
    listaOperacoes.innerHTML = "";

    salvas.forEach(op => {
        const card = document.createElement("div");
        card.className = "card";

        const titulo = document.createElement("h3");
        titulo.textContent = op.id + " - " + op.nome;

        const ul = document.createElement("ul");
        op.objetosCusto.forEach(codigo => {
            const li = document.createElement("li");
            const descricao = objetosCustoDescricao[codigo] || "Descrição não encontrada";
            li.innerHTML = `<strong>${codigo}</strong> - ${descricao}`;
            ul.appendChild(li);
        });

        const btn = document.createElement("button");
        btn.textContent = "Excluir";
        btn.addEventListener("click", () => excluirOperacao(op.id));

        card.appendChild(titulo);
        card.appendChild(ul);
        card.appendChild(btn);
        listaOperacoes.appendChild(card);
    });
}

function excluirOperacao(id) {
    let salvas = JSON.parse(localStorage.getItem("minhasOperacoes") || "[]");
    salvas = salvas.filter(op => op.id !== id);
    localStorage.setItem("minhasOperacoes", JSON.stringify(salvas));
    carregarOperacoes();
}

carregarOperacoes();

document.addEventListener("DOMContentLoaded", () => {
    const botaoNavegar = document.getElementById("bt_nav");

    if (botaoNavegar) {
        botaoNavegar.addEventListener("click", () => {
            const paginaAtual = window.location.pathname;

            if (paginaAtual.includes("index.html")) {
                window.location.href = "minhas-operacoes.html";
            } else if (paginaAtual.includes("minhas-operacoes.html")) {
                window.location.href = "index.html";
            }
        });
    }
});
